<h class="font2"><center><img class="img1" src="images/thomas.jpg" alt=""></center>Thomas Cole nacio en Lancashire, Inglaterra el 1 de febrero de 1801 y murio en Catskill, Nueva York el 11 de febrero de 1848 fue un pintor estadounidense de origen británico, fundador de la Escuela del río Hudson. Establecido en Nueva York en 1825, empezó a pintar paisajes de la zona del río Hudson, con el objetivo de lograr "un estilo elevado de paisaje" en el que el mensaje moral fuese equivalente al de la pintura de historia. Pintó también temas bíblicos.</h>
