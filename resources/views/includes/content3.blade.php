<h class="font2"><center><img class="img1" src="images/picasso.jpg" alt=""></center>Pablo Ruiz Picasso nacio en Málaga el 25 de octubre de 1881 y murio en Mougins el 8 de abril de 1973 fue un pintor y escultor español, creador, junto con Georges Braque, del cubismo.

Es considerado desde la génesis del siglo xx como uno de los mayores pintores que participaron en muchos movimientos artísticos que se propagaron por el mundo y ejercieron una gran influencia en otros grandes artistas de su tiempo. Sus trabajos están presentes en museos y colecciones de toda Europa y del mundo. Además, abordó otros géneros como el dibujo, el grabado, la ilustración de libros, la escultura, la cerámica y el diseño de escenografía y vestuario para montajes teatrales. También tiene una breve obra literaria.</h>
