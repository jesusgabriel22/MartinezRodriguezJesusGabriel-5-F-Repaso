<h class="font2"><center><img class="img1" src="images/claude.jpg" alt=""></center>Claude Monet nacio en París el 14 de noviembre de 1840 y murio en Giverny el 5 de diciembre de 1926 fue un pintor francés, uno de los creadores del impresionismo. El término impresionismo deriva del título de su obra Impresión, sol naciente (1872). Sus primeras obras, hasta la mitad de la década de 1860, son de estilo realista. Monet logró exponer algunas en el Salón de París. A partir del final de la década de 1860 comenzó a pintar obras impresionistas. Esta desviación del gusto de la época, que era marcado por las academias de arte, empeoró su situación económica a la vez que afianzó su decisión de continuar en ese azaroso camino.</h>
